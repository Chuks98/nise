                 <h3><b>HISTORY OF CORNELIA CONNELLY COLLEGE, NISE.</b></h3>
                 <p> The Society of the Holy Child Jesus is an Apostolic and International Congregation of Women Religious in the Roman Catholic Church. It was founded in 1846 in England by Venerable Cornelia Connelly, an American, with the spirit of simplicity, humility, charity, joy and accepted suffering. Cornelia Connelly’s concern for the wants of the age and works of Spiritual Mercy to be exercised led her to pay special attention to the training of Sisters for educational works. She set up her first convent in Derby, England; over the years, opened schools for the education of all classes of people in different parts of Europe and America. In 1930, the Sisters of the Society of the Holy Child Jesus (SHCJ) came to Africa. They began work in Calabar, Nigeria and has spread to different parts of Nigeria and some countries in Africa. <br> <br> <p>The Sisters of the Society of the Holy Child started in Catholic Diocese of Awka, Anambra State in 2011. The two pioneer Sisters Helen Ebede, Faustina Ejeagu lived in a rented apartment with other tenants in Agulu Anambra State. Sr Helen is a lecturer at Federal College of Education (Technical), Umunze while Sr Faustina was a teacher at Loretto Special Science Secondary School, Adazi Nnukwu. In 2016, Sisters Helen Ebede and Ogochukwu Ekwedike moved to Nri and lived in a rented apartment together with Sr. Chinyere Nwafor who was missioned to the community in 2018 as a student of Federal College of Education (Technical) Umunze Anambra State, until August 2020 when the Sisters’ Convent was completed in Nise by the Sisters of the Society of the Holy Child Jesus.</p><br> <p>The Sisters of the Society of the Holy Child Jesus saw the need to establish a secondary school, Cornelia Connelly College, for boys and girls that will be of national and international standards. The Sisters were able to acquire a parcel of land for the Cornelia Connelly College building project. The College building was designed by Arc. Chukwunonso Abanobi a member of Grandforms Consultants. The Sisters awarded the contract for the construction of the school building to Nahman Construction Company Ltd on 28th June, 2021. The building was completed and handed over to the Sisters on 3rd November, 2023. The school was not opened in 2023 because the Sisters needed to do the necessary preparations before starting the school.</p> <br> <p>The College building is an ultra-modern building of national and international standards. Cornelia Connely College is located at Ngodo Village-Nise, Awka South L.G.A. Anambra State. The site is located off a major access road leading to the city centre within the Awka Capital Territory. Access to the College is from this major road and leads directly into the college compound. The college building occupies 3734.14m2 land mass and there is more land for future developments. 
                    Cornelia Connelly College is planned for five hundred (500) students at its maximum capacity. By September, 2024/2025 the college hopes to enroll seventy-five (75) Junior Secondary School (JSS 1) students, twenty- five (25) students (boys and girls) per class. </p><br> <p>The college building has 18 classrooms, 9 classrooms for Junior Secondary School and 9 classrooms for Senior Secondary School. Other facilities in the college building include demonstration/TV Room/Indoor (1), Games/Kitchen/dining Hall (1), staff rooms (1), senior staff offices (2), Laboratories (4), General stores (2), multipurpose hall (1), library (1), Principal’s office & store room (1), accounts & store room (1), secretary’s office & store room (1), conference/board room (1), staff common room (1), counselling rooms (2), school shop (1), outdoor play area (1), sick bay (1), electricity supply (3 sources), generator house, borehole (1), overhead tanks and excellent toilet facilities. All these are provided as a way of meeting the holistic formation and educational needs of the students.</p> <br> <p>Cornelia Connelly College, Nise will be opened officially on 10th August, 2024. Academic activities will start on 2nd September, 2024. The pioneer Principal is Rev. Sister Clarence Uzogara, SHCJ. <p> <br> <b>The Mission Statement of the College is:</b> <br><i> Schools of the Society of the Holy Child Jesus share in the Mission of the Society of the Holy Child Jesus which is “to help others believe that GOD lives and acts in them and in our world, and to rejoice in the Divine Presence”.<br><br> The schools, are committed by heritage to the Educational Mission of the Catholic Church through the Philosophy and Spirituality of Cornelia Connelly, which is based on trust and reverence for the dignity of every person. We are committed to the holistic development of each child and to maintaining a high sense of religious and academic excellence, community and family spirit which is prevalent in all our schools. Our philosophy of education challenges each child to reach his or her potential. We encourage our students to respond to the “wants of the age” with compassion, integrity and confidence in God and in their God given gifts and talents.</i></p>
